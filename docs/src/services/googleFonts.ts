/**
 * Google Fonts integration service.
 *
 * Provides font search (from a curated list of popular families) and
 * font-buffer fetching.
 *
 * Font resolution uses a pre-generated manifest (`font-manifest.json`)
 * that maps font families to complete TTF URLs on fonts.gstatic.com.
 * The manifest is generated at build time by `docs/scripts/generate-font-manifest.ts`
 * using a spoofed User-Agent so Google returns complete TTF files (not
 * woff2 subsets).  This avoids needing a CORS proxy.
 *
 * For fonts not in the manifest, falls back to fetching the Google Fonts
 * CSS API directly (may return woff2 subset URLs with a browser UA).
 */

